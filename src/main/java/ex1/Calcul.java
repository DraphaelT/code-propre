package ex1;

/**
 * Classe qui fait different calcul entre a et b 
 * @author Thomas
 */
public class Calcul {

	/**
	 * Methode qui calcule en additionnent a et b
	 * @param a int a
	 * @param b int b
	 * @return int
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Methode qui calcule en soustrais a et b
	 * @param a int a
	 * @param b int b
	 * @return int
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}